    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String img = request.getParameter("avatanew");
        String name = request.getParameter("nname");
        String sum = request.getParameter("nsummary");
        AuthorDAO aDAO = new AuthorDAO();
        UserAcc a = (UserAcc) request.getSession().getAttribute("loginacc");
        BooksDAO bDAO = new BooksDAO();
        bDAO.createNewBook(aDAO.getAuthorByUserId(a.getUserId()).getAuthorId(), name, img, sum);
        request.setAttribute("notify", "Sussecfully!!!");
        request.getRequestDispatcher("createbook.jsp").forward(request, response);
       
    }

